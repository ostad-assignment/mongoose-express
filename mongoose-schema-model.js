const mongoose = require("mongoose")

// Define the schema
const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
})

// Create the model
const Product = mongoose.model("Product", productSchema)

module.exports = Product
